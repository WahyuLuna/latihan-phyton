
# KONVESI SUHU

print ("\n =====PROGRAM KONVERSI TEMPERATUR===== \n")

celcius = float (input("Masukan suhu Celcius = "))
print ("Suhu adalah ",celcius , "celcius")

#konversi ke reamur

reamur = (4/5)*celcius
print ("suhu dalam bentuk reamur adalah = ",reamur , "reamur")

#konversi ke farhenheit
farhenheit = ((9/5)*celcius) + 32
print ("suhu dalam bentuk farhenheit adalah = ",farhenheit , "farhenheit")

#konversi ke kelvin
kelvin = celcius + 273
print ("suhu dalam bentuk kelvin adalah = ",kelvin , "kelvin")








