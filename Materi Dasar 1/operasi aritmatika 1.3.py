#operasi aritmateka

Angka_A = 12
Angka_B = 12

#penjumlahan '+'
hasil = Angka_A + Angka_B

print("Hasil penjumlahan A + B = ",hasil )

#pengurangan '-'
hasil = Angka_A - Angka_B

print("Hasil pengurangan A - B = ",hasil )

#pembagian '/'
hasil = Angka_A / Angka_B

print("Hasil pembagian A / B = ",hasil )

#perkalian '*'
hasil = Angka_A * Angka_B

print("Hasil perkalian A X B = ",hasil )

#exponen '**'
hasil = Angka_A ** Angka_B

print("Hasil A pangkat B = ",hasil )

#pembagian '%'
hasil = Angka_A % Angka_B

print("Hasil aritmatika A % B = ",hasil )

#Division '//'
hasil = Angka_A // Angka_B

print("Hasil aritmatika A // B = ",hasil )

